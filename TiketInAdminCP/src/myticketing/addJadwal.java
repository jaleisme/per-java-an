    private void addAudi() {
        String sql = "Select no_audi from auditorium";
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/selfticket_bioskop", "root", "");
            ps = con.prepareStatement(sql);
            res = ps.executeQuery(sql);
            while(res.next()){
                auditorium.addItem(res.getString("no_audi"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(addFilm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
