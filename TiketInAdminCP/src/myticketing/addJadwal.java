    private void jadwalTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jadwalTableMouseClicked
        // TODO add your handling code here:
        int row = jadwalTable.rowAtPoint(evt.getPoint());
        String ids = jadwalTable.getValueAt(row, 0).toString();
        String idFilm = jadwalTable.getValueAt(row, 1).toString();
        String idAudi = jadwalTable.getValueAt(row, 2).toString();
        String time = jadwalTable.getValueAt(row, 3).toString();
        String tanggal = jadwalTable.getValueAt(row, 4).toString();
        
        id.setText(ids);
        film_judul.setSelectedItem(idFilm);
        auditorium.setSelectedItem(idAudi);
        times.setText(time);
        dates.setText(tanggal);
    }//GEN-LAST:event_jadwalTableMouseClicked

